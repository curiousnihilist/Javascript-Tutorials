// Operations on different data type

// Integer
console.log(45+32);
console.log(45-32);
console.log(45*32);
console.log(45/32);
console.log(45%32);

console.log(45/0);

// // String
var name = "akash";
var surname = "verma";
console.log(name+surname);
console.log(name-surname);
console.log(name*surname);
console.log(name/surname);

console.log(name + 2);
console.log(name - 2);
console.log(name * 2);
console.log(name / 2);

// Boolean

console.log(true+false);  // true == 1 false == 0
console.log(true-false);
console.log(true && false);
console.log(true || false);
console.log(true == 1);
console.log(false == 0);

